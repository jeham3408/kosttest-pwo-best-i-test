function scoreDose(doseMg: number | null | undefined, rule: IngredientRule): number {
  const dose = doseMg ?? 0
  if (dose <= 0) return 0

  const cDose = rule.cDoseMg
  const aDose = rule.aDoseMg
  const bDose = cDose + (aDose - cDose) / 2

  if (dose >= aDose) return 1

  if (dose >= bDose) {
    const t = (dose - bDose) / (aDose - bDose)
    return 0.85 + t * 0.15
  }

  if (dose >= cDose) {
    const t = (dose - cDose) / (bDose - cDose)
    return 0.7 + t * 0.15
  }

  if (dose >= cDose * 0.5) {
    const t = (dose - cDose * 0.5) / (cDose * 0.5)
    return 0.45 + t * 0.25
  }

  if (dose >= cDose * 0.25) {
    const t = (dose - cDose * 0.25) / (cDose * 0.25)
    return 0.2 + t * 0.25
  }

  const t = dose / (cDose * 0.25)
  return 0 + t * 0.2
}

function letterFromScoreValue(score: number): GradeLetter {
  if (score >= 0.85) return 'A'
  if (score >= 0.7) return 'B'
  if (score >= 0.5) return 'C'
  if (score >= 0.3) return 'D'
  if (score >= 0.1) return 'E'
  return 'F'
}

function doseFor(product: TestedProduct, key: IngredientKey): number | null {
  if (key === 'lCitrullineEq') {
    const citrulline = product.citrullineMg ?? 0
    const arginine = product.extraDoses?.arginine ?? 0
    const beetroot = product.extraDoses?.beetroot ?? 0
    return citrulline + arginine * 0.5 + beetroot * 0.9
  }
  if (key === 'betaAlanine') return product.betaAlanineMg
  if (key === 'glycerol') return product.glycerolMg
  return product.extraDoses?.[key] ?? null
}

function formatDose(doseMg: number | null | undefined) {
  if (doseMg === null || doseMg === undefined) return '0 mg'
  return `${doseMg.toLocaleString('nb-NO')} mg`
}

function letterFromScore(score: number): GradeLetter {
  if (score >= 61) return 'A'
  if (score >= 49) return 'B'
  if (score >= 36) return 'C'
  if (score >= 24) return 'D'
  if (score >= 12) return 'E'
  return 'F'
}

function nauseaPenalty(product: TestedProduct) {
  const arginine = product.extraDoses?.arginine ?? 0
  if (arginine > 20000) return 8
  if (arginine > 15000) return 5
  if (arginine >= 10000) return 3
  return 0
}

export type GradeOptions = {
  excludeBetaAlanine?: boolean
}

export function calculateProductGrade(product: TestedProduct, options?: GradeOptions) {
  const filteredRules = options?.excludeBetaAlanine
    ? ingredientRules.filter((r) => r.key !== 'betaAlanine')
    : ingredientRules

  const ingredientBreakdown = filteredRules.map((rule) => {
    const dose = doseFor(product, rule.key)
    const raw = scoreDose(dose, rule)
    const points = raw * rule.weight
    const grade = letterFromScoreValue(raw)

    return {
      key: rule.key,
      label: rule.label,
      doseLabel: formatDose(dose),
      grade,
      points,
      maxPoints: rule.weight,
      note: rule.note,
    } satisfies GradeBreakdown
  })

  const hasBitterOrange = product.extraDoses?.bitterOrange ? 1 : 0
  const hasBVitamins = product.extraDoses?.bVitamins ? 1 : 0

  const beetrootSynergy =
    (product.citrullineMg ?? 0) + (product.extraDoses?.beetroot ?? 0) >= 13000 ? 2 : 0

  const rawScore =
    ingredientBreakdown.reduce((sum, item) => sum + item.points, 0) +
    beetrootSynergy +
    hasBitterOrange +
    hasBVitamins -
    nauseaPenalty(product)
  const score = Math.max(0, Math.min(100, Math.round(rawScore)))

  return {
    score,
    overallGrade: letterFromScore(score),
    gradeBreakdown: ingredientBreakdown,
  }
}

