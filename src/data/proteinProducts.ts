function gradeProduct(raw: RawProtein): TestedProteinProduct {
  const graded = calculateProteinGrade({
    sourceType: raw.sourceType,
    priceNok: raw.priceNok,
    packageSizeG: raw.packageSizeG,
    proteinPer100g: raw.proteinPer100g,
    officialDiaas: raw.officialDiaas,
  })
  const leucinePer30g = raw.leucinePerServingG != null
    ? (raw.leucinePerServingG / raw.proteinPerServingG) * 30
    : 2.5
  return { ...raw, ...graded, leucinePer30gProtein: Math.round(leucinePer30g * 10) / 10, rank: 0 }
}

export const testedProteinProducts: TestedProteinProduct[] = rawProducts
  .map(gradeProduct)
  .sort((a, b) => b.score - a.score || a.pricePerGramProtein - b.pricePerGramProtein)
  .map((product, index) => {
    const qItem = proteinVerificationQueue.queue.find((item) => item.id === product.id)
    return {
      ...product,
      rank: index + 1,
      award: index === 0 ? product.award : index < 3 ? product.award : '',
      verificationStatus: getProteinVerificationStatus(product.id),
      verifiedAt: qItem?.verifiedAt,
    }
  })

